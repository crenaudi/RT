#include "../altlib.h"

void	ft_memset(void *data, int c, size_t n)
{
	uint8_t *data_ptr;

	data_ptr = (uint8_t *)data;
	while (n--)
	{
		data_ptr[n] = (uint8_t)c;
	}
}
